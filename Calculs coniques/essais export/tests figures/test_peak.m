function wb=wfpeaks

f=figure(100);
set(f,'Visible', 'off'),

h=axes('OuterPosition',[0 0 0.85 1]);

s = uicontrol(...
'Parent',f,...
'Units','normalized',...
'FontUnits','pixels',...
'BackgroundColor',[0.9 0.9 0.9],...
'FontSize',12.8888888888889,...
'Max',100,...
'Min',20,...
'Position',[0.0624203821656051 0.921052631578947 0.871337579617834 0.0325077399380805],...
'String','gamma',...
'Style','slider',...
'SliderStep',[0.1 0.1],...
'Tag','slider1');
max=get(s,'Max');
step=1/max;
set(s,'Value',20,'SliderStep', [step/10, step]);

tb = uicontrol(...
'Parent',f,...
'Units','normalized',...
'FontUnits','pixels',...
'FontSize',12.8888888888889,...
'Position',[0.789808917197452 0.843653250773994 0.182165605095541 0.0541795665634675],...
'String','Refresh',...
'Style','togglebutton',...
'Tag','togglebutton1',...
'Value', 1);

drawnow;

while get(tb,'value')==1;
    pause(5);
    a=get(s,'Value');
    p=peaks(a);
    surf(p);
    drawnow;
    wb=webfigure(f);
end
